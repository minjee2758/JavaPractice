package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a+b;
        System.out.println("a + b = " + sum);

        int diff = a - b;
        System.out.println(diff);

        int div = a/b;
        System.out.println(div);

        int mod = a%b;
        System.out.println(mod);
    }
}
